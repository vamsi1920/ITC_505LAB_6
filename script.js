function bubbleSort(arr) {
    let len = arr.length;
    for (let i = 0; i < len - 1; i++) {
        for (let j = 0; j < len - 1 - i; j++) {
            if (arr[j] > arr[j + 1]) {
                [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
            }
        }
    }
    return arr;
}

function sortUserArray() {
    let inputElement = document.getElementById("userInput");
    let outputElement = document.getElementById("sortedArray");
    let inputArray = inputElement.value.split(',').map(Number);
    let sortedArr = bubbleSort(inputArray);
    outputElement.textContent = JSON.stringify(sortedArr);
}
